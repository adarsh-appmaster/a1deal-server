export const STATE_LIST = Object.keys(INDIA_STATES).sort();

export function getCities(state) {
  return INDIA_STATES[state] || [];
}

// Flat list of all cities + areas for quick search
export const ALL_LOCATIONS = Object.values(INDIA_STATES).flat();

export function searchLocations(query) {
  if (!query?.trim()) return [];
  const q = query.trim().toLowerCase();
  const results = [];
  for (const [state, locs] of Object.entries(INDIA_STATES)) {
    for (const loc of locs) {
      if (loc.toLowerCase().startsWith(q)) results.push({ label: loc, state });
    }
  }
  for (const [state, locs] of Object.entries(INDIA_STATES)) {
    for (const loc of locs) {
      if (!loc.toLowerCase().startsWith(q) && loc.toLowerCase().includes(q)) {
        results.push({ label: loc, state });
      }
    }
  }
  return results.slice(0, 20);
}
